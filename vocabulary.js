// 保存到本地存储的函数
function saveVocabularyData() {
    localStorage.setItem('vocabularyData', JSON.stringify(vocabulary));
}

// 从本地存储加载数据的函数
function loadVocabularyData() {
    const savedData = localStorage.getItem('vocabularyData');
    if (savedData) {
        // 合并保存的用户进度与原始词汇数据
        const userProgress = JSON.parse(savedData);
        
        // 更新原始词汇的状态
        for (let i = 0; i < vocabulary.length; i++) {
            const savedWord = userProgress.find(w => w.word === vocabulary[i].word);
            if (savedWord) {
                vocabulary[i].status = savedWord.status;
                vocabulary[i].difficulty = savedWord.difficulty;
                vocabulary[i].nextReview = savedWord.nextReview;
                vocabulary[i].reviewCount = savedWord.reviewCount;
                vocabulary[i].isWrong = savedWord.isWrong || false;  // 保存错题状态
            }
        }
    }
    return vocabulary;
}

// 导出词汇数据
